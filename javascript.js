// indice do arrayObjetos
obj = 0

function executar(){

    // array com todas as perguntas
    arrayObjetos = [pergunta1, pergunta2, pergunta3, pergunta4, pergunta5, pergunta6, pergunta7, 
    pergunta8, pergunta9, pergunta10, pergunta11, pergunta12, pergunta13, pergunta14, pergunta15]    

    var img = document.createElement("img")
    img.setAttribute("src", arrayObjetos[obj].imagem)

    var question = document.createElement("h4")
    question.textContent = arrayObjetos[obj].pergunta
    
    for(i = 0; i < arrayObjetos[obj].alternativas.length; i++){

        var button = document.createElement("button")       
        button.textContent = arrayObjetos[obj].alternativas[i]
        button.value = arrayObjetos[obj].alternativas[i]
        button.setAttribute("onclick", "verificar(value)")    

        var divImg = document.getElementById("divImg")
        divImg.appendChild(img)
        var divPergunta = document.getElementById("divPergunta")
        divPergunta.appendChild(question)
        var divOptions = document.getElementById("divOptions")
        divOptions.appendChild(button)
           
        
    }  
}


// Executar Ao carregar a pagina
function comecar(){    
    document.getElementById("divComecar").innerHTML = ""
    executar()   
}

